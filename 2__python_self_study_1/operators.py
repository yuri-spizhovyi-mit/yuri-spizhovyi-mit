# -- string concatenation --
hw = "hello" + " " + "world"  # "hello world"
print(hw)
# -- logical (or boolean) operators --
print(True and False)  # False
print(True or False)  # True
print(not True)  # False

# -- arithmetic: int int --
# addition
add = 4 + 2  # 6
print(add)
# subtraction
sub = 4 - 2  # 2
print(sub)

# -- arithmetic: float float --
# addition
print(f"4.0 + 2.0 =  {4.0 + 2.0}")  # 6.0
# subtraction
print("4.0 - 2.0 = ", 4.0 - 2.0)  # 2.0

# -- arithmetic: mixed --
# addition
print(f"4 + 2.0 = {4 + 2.0}") # 6.0
# subtraction
print(f"4.0 - 2 =  {4.0 - 2}")  # 2.0

# -- comparisons --
# equality
print(4 == "4")  # False
# inequality
4 != "4"  # True
# greater than
4 > 3  # True
4 > 4  # False
# less than
4 < 4  # False
4 < 5  # True
# greater than or equal to
4 >= 3  # True
4 >= 4  # True
4 >= 5  # False
# less than or equal to
4 <= 3  # False
4 <= 4  # True
4 <= 5  # True
